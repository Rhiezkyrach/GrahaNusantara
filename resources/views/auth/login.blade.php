<script>
function togglePassword() {
    let input_pass = document.getElementById("password");
    let closed_eyes = document.getElementById("closed_eyes");
    let opened_eyes = document.getElementById("opened_eyes");

    if (input_pass.type === "password") {
        input_pass.type = "text";
        closed_eyes.classList.add("hidden");
        opened_eyes.classList.remove("hidden");
    } else {
        input_pass.type = "password";
        closed_eyes.classList.remove("hidden");
        opened_eyes.classList.add("hidden");
    }
}
</script>

